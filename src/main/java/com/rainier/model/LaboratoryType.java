package com.rainier.model;

public class LaboratoryType {
    private Integer id;
    private String name;
    private Integer imgDate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getImgDate() {
        return imgDate;
    }

    public void setImgDate(Integer imgDate) {
        this.imgDate = imgDate;
    }
}
